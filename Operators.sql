CREATE TABLE IF NOT EXISTS TATA (
    TATA_id TEXT PRIMARY KEY,
    MODEL_NAME TEXT NOT NULL,
    YEAR INTEGER,
    UNIT INTEGER,
    PRICE REAL
);

